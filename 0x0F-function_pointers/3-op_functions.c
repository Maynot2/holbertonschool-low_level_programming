/**
  * op_add - Computes the sum of 2 integers a and b.
  * @a: An integer.
  * @b: An integer.
  *
  * Return: the sum of a and b as an integer.
  *
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - Computes the difference of 2 integers a and b.
  * @a: An integer.
  * @b: An integer.
  *
  * Return: the difference of a and b as an integer.
  *
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - Computes the multiplication of 2 integers a and b.
  * @a: An integer.
  * @b: An integer.
  *
  * Return: the result of multiplying a and b as an integer.
  *
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - Computes the division of 2 integers a and b.
  * @a: An integer.
  * @b: An integer.
  *
  * Return: the result of dividing a and b as an integer.
  *
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - Computes the remainder of divinding 2 integers a and b.
  * @a: An integer.
  * @b: An integer.
  *
  * Return: the remainder of divinding a and b as an integer.
  *
  */

int op_mod(int a, int b)
{
	return (a % b);
}
